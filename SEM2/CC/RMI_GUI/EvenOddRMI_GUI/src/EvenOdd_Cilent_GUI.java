            int num = Integer.parseInt(jTextField1.getText());

            EvenOdd_Interface evenOddChecker = (EvenOdd_Interface) Naming.lookup("evenOdd");
            boolean result = evenOddChecker.isEven(num);

            if (result)
                jLabel4.setText(num + " is an Even number!");
            else
                jLabel4.setText(num + " is an Odd number!");

